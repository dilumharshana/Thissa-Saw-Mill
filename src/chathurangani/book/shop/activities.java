     public static void dealItemsToTable(String [] dealData)
        {
            DefaultTableModel delas = (DefaultTableModel) cashOuts.getModel();
            delas.addRow(dealData);
        
        }
     
     public static void clearTable()
     {
            DefaultTableModel delas = (DefaultTableModel) cashOuts.getModel();
            while(cashOuts.getRowCount() >0 )
                {
                    delas.removeRow(0);
                }
     }
     
