    public static void cashouts_ToTable(String [] dealData)
        {
            DefaultTableModel deals = (DefaultTableModel) cashOuts.getModel();
            String [] linebrake = {"",""};
            deals.addRow(linebrake);
            deals.addRow(dealData);
        
        }
    
