    //METHOD FOR CHEKING ENTERD BARCODE OR BILL NO FROM THE DATABASE
    void cheker() {

        String dealcode = dealIdBox.getText();
        try {

            //CLEARING TABLE 
            DefaultTableModel removedata = (DefaultTableModel) returnItemTable.getModel();
            int itemrow = returnItemTable.getRowCount();//Getting number of rows in item table to clear that all.

            while (removedata.getRowCount() > 0) {
                removedata.removeRow(0); //0 means indes of the raw [o th raw will be remove in the table]
            }

            resetData(); // restet all btn feilds
            dealDataClear(); // clear customer details

            dealIdBox.setText(dealcode); // Displaying again cleard item code in to deal chek box

            //STARTING SEARCH
            String[] name_date = connection.dealCheker(dealcode); //excpting name and datetime as a items ofa assry by calling to method in array
            totalBox.setText(name_date[2]); // setting the 1 index of returned array 'deal dqate and time' to date time name box
            discountBox.setText(name_date[3]); // setting the 2 index of returned array 'total amount of the bill' to totalamount box
            paymentBox.setText(name_date[4]);
            balanceBox.setText(name_date[5]);

            CancelBtn.setEnabled(true);
            returnAllbtn.setEnabled(true);
            cashBackBtn.setEnabled(true);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Something went wrong !");
        }

    }
