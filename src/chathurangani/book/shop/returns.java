    private void fullCashBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fullCashBackActionPerformed
        try {
            DefaultTableModel fullcash = (DefaultTableModel) returnItemTable.getModel();
            int rowcount = fullcash.getRowCount();
            for (int i = 0; i < rowcount; i++) {
                
                //generating total cash price of items
                BigDecimal item_Cprice = new BigDecimal(connection.searchItemPrice(returnItemTable.getValueAt(i, 0).toString())).multiply(new BigDecimal(returnItemTable.getValueAt(i, 2).toString()));
                cashPrice = cashPrice.add(item_Cprice);

                //updating stock items
                int stock = connection.searchItemStock(returnItemTable.getValueAt(i, 0).toString()); //cheking for current stock
                stock = stock + Integer.parseInt(returnItemTable.getValueAt(i, 2).toString()); //updatin stock
                connection.updateStockitems("stock", String.valueOf(stock), returnItemTable.getValueAt(i, 0).toString());
                
                
                resetData();
                dealDataClear();
            }

            boolean dateAvailability = connection.passdate(String.valueOf(java.time.LocalDate.now()));

            //if date is not exsit in database then create new row for the date either update exsising dat row
            if (dateAvailability == true) {
                // adding new cashprice amount and sellprice amount to prevois cashprice and sell price amount
                //new cash price and sell price amounts are holded in controller class cashPrice and sellPrice variables
                BigDecimal updated_outPrice = new BigDecimal("0.0");
                BigDecimal updated_outsell = new BigDecimal("0.0");
                try 
                    {
                        updated_outPrice = cashPrice.add(controllers.out);
                    }
                catch(Exception e)
                    {
                        updated_outPrice = new BigDecimal("0.0");
                    }
                 try 
                    {
                        updated_outsell = new BigDecimal(totalBox.getText().trim()).add(controllers.sell);
                    }
                catch(Exception e)
                    {
                        updated_outsell = new BigDecimal("0.0");
                    }
                
                
                connection.incomedata_outUpdater(String.valueOf(updated_outPrice), String.valueOf(updated_outsell), String.valueOf(java.time.LocalDate.now()));
                JOptionPane.showMessageDialog(null, "CASH BACK AMOUNT IS \n Rs . " + totalBox.getText()); // setting non discounted value to total display box
                cashPrice = new BigDecimal("0.0");
                totalDisplay.setText("0.0");
            } else {
                connection.Strore_incomedata("0.0", "0.0", cashPrice.toString(), totalBox.getText().trim(), String.valueOf(java.time.LocalDate.now()));
                JOptionPane.showMessageDialog(null, "CASH BACK AMOUNT IS \n Rs . " + totalBox.getText()); // setting non discounted value to total display box
                cashPrice = new BigDecimal("0.0");
                totalDisplay.setText("0.0");
            }
        } catch (Exception ex) {
            Logger.getLogger(MAIN_FRAME.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_fullCashBackActionPerformed

